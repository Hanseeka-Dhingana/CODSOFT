    private boolean enterData(){
       if(name_txt.getText().trim().isEmpty()){
               String message = "Please Enter Student Name";
               JLabel messageLabel = new JLabel(message);
               messageLabel.setFont(new Font("Trebuchet MS", Font.PLAIN, 24)); 
               JOptionPane.showMessageDialog(null, messageLabel);       
               name_txt.grabFocus();
               return false;
        }
       if(roll_txt.getText().trim().isEmpty())
        {
               String message = "Please Enter Student Roll Number";
               JLabel messageLabel = new JLabel(message);
               messageLabel.setFont(new Font("Trebuchet MS", Font.PLAIN, 24)); 
               JOptionPane.showMessageDialog(null, messageLabel);       
               roll_txt.grabFocus();
               return false;
        }
       if(dept_txt.getText().trim().isEmpty())
        {       
               String message = "Please Enter Student Department";
               JLabel messageLabel = new JLabel(message);
               messageLabel.setFont(new Font("Trebuchet MS", Font.PLAIN, 24)); 
               JOptionPane.showMessageDialog(null, messageLabel);       
               dept_txt.grabFocus();
               return false;
        }
       if(grade_txt.getText().trim().isEmpty())
        {
               String message = "Please Enter Student Grade";
               JLabel messageLabel = new JLabel(message);
               messageLabel.setFont(new Font("Trebuchet MS", Font.PLAIN, 24)); 
               JOptionPane.showMessageDialog(null, messageLabel);       
               grade_txt.grabFocus();
               return false;
        }
        return true;
    }
            
    private void clearData()
    {
        name_txt.setText("");          roll_txt.setText("");
        dept_txt.setText("");          grade_txt.setText("");
    }
