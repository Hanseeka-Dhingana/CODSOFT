    private void add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btnActionPerformed
       if(enterData())
       {
               String name = name_txt.getText();
               int roll_Num = Integer.valueOf(roll_txt.getText());
               String dept = dept_txt.getText();
               String grade = grade_txt.getText();
       
        
       try{
               Connection connection = ConnectionProvider.getCon();
               Statement statement = connection.createStatement();
           
               statement.executeUpdate("insert into student_registor(Student_name,Roll_number,Student_depart,Student_grade) Values('"+name+"','"+roll_Num+"','"+dept+"','"+grade+"')");
               String message = "Data Successfully Entered";
               JLabel messageLabel = new JLabel(message);
               messageLabel.setFont(new Font("Trebuchet MS", Font.PLAIN, 24)); 
               JOptionPane.showMessageDialog(null, messageLabel);       
             
       }
       catch(Exception e)
       {
               JOptionPane.showMessageDialog(null, e);
       }
        
          clearData();
       }
        
    }//GEN-LAST:event_add_btnActionPerformed

